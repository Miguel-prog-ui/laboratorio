<h1>Ejemplo de operaciones aritmeticas en PHP</h1> 
<?php 
 
$a = 8; 
$b = 3; 
echo "Suma", "<br>";
echo "8 + 3 " , " = ",  $a + $b,  "<br>"; 
echo "Resta", "<br>";
echo "8 - 3 " , " = ", $a - $b, "<br>";
echo "Multiplicacion", "<br>";
echo "8 * 3 " , " = ", $a * $b, "<br>"; 
echo "Division", "<br>";
echo "8 / 3 " , " = ", $a / $b, "<br>"; 
 
$a++ ; 
echo $a,"<br>"; 
$b--; 
echo $b,"<br>"; 
 
?> 
 
